"use client";
import React from "react";
import { gsap } from "gsap";

const healthFaqs = [
  {
    id: 1,
    question: "How can I boost my immune system naturally?",
    answer:
      "Maintain a balanced diet, get regular exercise, sleep well, and manage stress to naturally boost immunity.",
  },
  {
    id: 2,
    question: "What are the benefits of regular exercise?",
    answer:
      "Regular exercise improves cardiovascular health, strengthens muscles, boosts mood, and increases energy levels.",
  },
  {
    id: 3,
    question: "How much water should I drink daily?",
    answer:
      "Aim to drink at least 8 glasses (about 2 liters) of water daily, but individual needs may vary based on activity and climate.",
  },
  {
    id: 4,
    question: "What are some effective ways to reduce stress?",
    answer:
      "Techniques like meditation, deep breathing, yoga, and spending time in nature help reduce stress effectively.",
  },
  {
    id: 5,
    question: "How important is sleep for overall health?",
    answer:
      "Sleep is crucial for mental and physical health; adults should aim for 7-9 hours per night to function optimally.",
  },
];

const HealthFaq = () => {
  const activeIdRef = React.useRef(null);
  const containersRef = React.useRef({});

  const toggleFaq = (id) => {
    if (activeIdRef.current === id) {
      const container = containersRef.current[id];
      if (container) {
        gsap.to(container, {
          height: 0,
          opacity: 0,
          duration: 0.35,
          ease: "power2.out",
          onComplete: () => {
            container.style.display = "none";
          },
        });
        activeIdRef.current = null;
      }
    } else {
      if (activeIdRef.current !== null) {
        const prevContainer = containersRef.current[activeIdRef.current];
        if (prevContainer) {
          gsap.to(prevContainer, {
            height: 0,
            opacity: 0,
            duration: 0.35,
            ease: "power2.out",
            onComplete: () => {
              prevContainer.style.display = "none";
            },
          });
        }
      }

      const container = containersRef.current[id];
      if (container) {
        container.style.display = "block";
        const height = container.scrollHeight;
        gsap.fromTo(
          container,
          { height: 0, opacity: 0 },
          {
            height,
            opacity: 1,
            duration: 0.4,
            ease: "power2.out",
            onComplete: () => {
              container.style.height = "auto";
            },
          }
        );
      }
      activeIdRef.current = id;
    }
  };

  return (
    <section className="bg-white text-black py-12 px-4 my-5">
      <div className="max-w-4xl mx-auto">
        <h2 className="text-4xl font-bold mb-8 text-center">Health FAQs</h2>
        <div className="space-y-4">
          {healthFaqs.map(({ id, question, answer }) => (
            <div
              key={id}
              className="border border-green-yellow rounded-lg overflow-hidden"
            >
              <button
                className="w-full text-left px-6 py-4 flex justify-between items-center font-semibold text-lg hover:bg-black transition"
                onClick={() => toggleFaq(id)}
                aria-expanded={activeIdRef.current === id}
                aria-controls={`faq-answer-${id}`}
                id={`faq-question-${id}`}
              >
                {question}
                <svg
                  className={`w-6 h-6 transition-transform duration-300 ${
                    activeIdRef.current === id ? "rotate-180" : ""
                  }`}
                  fill="none"
                  stroke="currentColor"
                  strokeWidth="2"
                  viewBox="0 0 24 24"
                  xmlns="http://www.w3.org/2000/svg"
                  aria-hidden="true"
                >
                  <path
                    strokeLinecap="round"
                    strokeLinejoin="round"
                    d="M19 9l-7 7-7-7"
                  ></path>
                </svg>
              </button>
              <div
                ref={(el) => (containersRef.current[id] = el)}
                id={`faq-answer-${id}`}
                role="region"
                aria-labelledby={`faq-question-${id}`}
                className="px-6 overflow-hidden text-black"
                style={{ display: "none", height: 0, opacity: 0 }}
              >
                <p className="py-4">{answer}</p>
              </div>
            </div>
          ))}
        </div>
      </div>
    </section>
  );
};

export default HealthFaq;
